				rules: {
					nom: {
						required: true,
                        minlength: 5
					},
					mdp: {
						required: true,
						minlength: 8
					},
					cmdp: {
						required: true,
						minlength: 8,
						equalTo: "#mdp"
					},
					mail: {
						required: true,
                        email: true
					},
                    terms: "required",
                    bday: "required"
				},
				messages: {
					nom: {
						required: "Veuillez entrer un nom",
						minlength: "Votre nom doit contenir au moins 5 caractères"
					},
					mdp: {
						required: "Veuillez un mot de pass",
						minlength: "Votre pass doit contenir au moins 8 caractères"
					},
					cmdp: {
						required: "Veuillez un mot de pass",
						minlength: "Votre pass doit contenir au moins 8 caractères",
						equalTo: "Mot de pass non similaire"
                    },
                    required: "Veuillez entrer un email",
					mail: "Entrer un email Valide",
					terms: "Vous devez Accepter les termes d'utilisation"
				},
				errorElement: "em",
				errorPlacement: function ( error, element ) {
					// Add the `help-block` class to the error element
					error.addClass( "help-block" );

					// Add `has-feedback` class to the parent div.form-group
					// in order to add icons to inputs
					element.parents( ".champs" ).addClass( "has-feedback" );

					if ( element.prop( "type" ) === "checkbox" ) {
						error.insertAfter( element.parent( "label" ) );
					} else {
						error.insertAfter( element );
					}

					// Add the span element, if doesn't exists, and apply the icon classes to it.
					if ( !element.next( "i" )[ 0 ] ) {
						$( "<i class='fa fa-times Novalid form-control-feedback'></>" ).insertAfter( element );
					}
				},
				success: function ( label, element ) {
					// Add the span element, if doesn't exists, and apply the icon classes to it.
					if ( !$( element ).next( "i" )[ 0 ] ) {
						$( "<i class='fa fa-check form-control-feedback'></i>" ).insertAfter( $( element ) );
					}
				},
				highlight: function ( element, errorClass, validClass ) {
					$( element ).parents( ".champs" ).addClass( "has-error" ).removeClass( "has-success" );
					$( element ).next( "i" ).addClass( "fa-times Novalid" ).removeClass( "fa-check" );
				},
				unhighlight: function ( element, errorClass, validClass ) {
					$( element ).parents( ".champs" ).addClass( "has-success" ).removeClass( "has-error" );
					$( element ).next( "i" ).addClass( "fa-check" ).removeClass( "fa-times" );
				}
			} );
		} );
	</script>

